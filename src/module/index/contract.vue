<p class="p3"></p>
<p class="p5">
<span class="s2">7.​&nbsp;</span><span>逾期违约金：指用户未在每月应付款日足额支付应付款项，商户或其指定的债权受让人依照开汇制定并在相关服务页面展示的收费规则向用户收取的一定比例的费用。</span>
