<p class="p3">
<span>除非本协议正文另有明确所指，在本协议中所用下列术语定义如下：</span>
</p>
<p class="p3"></p>
<p class="p5">
<span class="s2">1.​&nbsp;</span><span>消费分期本金：指用户在开汇合作商户向交易对手购买商品或服务时，使用开汇保理为其提供授信服务，选择以分期付款的方式支付全部或部分的订单款。分期支付的全部或部分订单款金额即为消费分期本金。</span>
</p>
<p class="p6"></p>
<p class="p5">
<span class="s2">2.​&nbsp;</span><span>应付款项：指用户累计未支付的消费分期本金、累计已产生且未支付的分期服务费、逾期违约金及其他本附件协议项下产生的有关款项，包括但不限于商户为实现债权的费用，如诉讼费、律师代理费、催收费用等。</span>
</p>
<p class="p6"></p>
<p class="p5">
<span class="s2">3.​&nbsp;</span><span>应付款日：用户应在约定付款日的24点前（含当日）支付全部应付款项，如逾期，将影响用户的征信记录。在延后付款方式下，应付款日是用户承诺的最晚付款日期；在分期付款方式下，到期付款日为用户每一期款项应支付的最晚日期。</span>
</p>
<p class="p7"></p>
<p class="p5">
<span class="s2">4.​&nbsp;</span><span>指定账户：指用户用于签署确认本附件协议、支付本附件协议项下应付款项等所必需的用户在开汇关联公司、合作商户、合作平台网站上绑定的本人账户。</span>
</p>
<p class="p7"></p>
<p class="p5">
<span class="s2">5.​&nbsp;</span><span>延后付款：指用户在使用开汇消费分期服务时确定的一个日期，用户承诺在该日期之前（含当日）支付全部消费分期本金的一种付款方式。</span>
</p>
<p class="p7"></p>
<p class="p5">
<span class="s2">6.​&nbsp;</span><span>延后付款期：指用户选择延后付款时，从消费记账日起至到期付款日之间的实际天数，根据用户的资信状况综合评估核定。</span>
